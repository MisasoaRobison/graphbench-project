    seeds = []

    # 1) Pure violation (baseline from project statement)
    seeds.append(Program(terms=[(1.0, "violation")]))

    # 2) Example given in the project statement
    seeds.append(Program(terms=[
        (10.0, "violation"),
        (0.3, "n"),  # diam not exposed directly; use n proxy
        (0.2, "Delta"),
        (0.1, "triangles_proxy"),
        (-0.05, "n"),
        (-0.2, "density_balance"),
    ]).cleaned())

    # 3) Current static heuristic (rough port of src/scoring.py)
    seeds.append(Program(terms=[
        (1.0, "violation"),
        (0.001, "n"),
        (0.05, "leaves"),
        (0.005, "m"),
        (1.0, "density_balance"),
        (-0.5, "isolated_penalty"),
    ]).cleaned())

    # 4) Aggressive structural seed
    seeds.append(Program(terms=[
        (1.0, "violation"),
        (0.02, "n"),
        (0.1, "leaves"),
        (0.05, "Delta"),
        (-0.01, "isolated_penalty"),
        (0.3, "xy_gap"),
    ]).cleaned())

    # 5) Compact seed (penalises large graphs)
    seeds.append(Program(terms=[
        (1.0, "violation"),
        (-0.05, "n"),
        (0.2, "xy_gap"),
        (0.5, "density_balance"),
    ]).cleaned())

    return seeds
